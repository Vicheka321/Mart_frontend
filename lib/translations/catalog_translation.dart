  static String translate(String? value) {
    final original = value?.trim() ?? '';
    if (original.isEmpty) return original;

    final lang = Get.locale?.languageCode ?? 'en';
    if (lang == 'en') return original;

    final normalized = _normalize(original);
    final exact = _phraseTranslations[lang]?[normalized];
    if (exact != null) return exact;

    final wordMap = _wordTranslations[lang];
    if (wordMap == null) return original;

    final translated = original.replaceAllMapped(
      RegExp(r'[A-Za-z]+'),
      (match) => wordMap[match.group(0)!.toLowerCase()] ?? match.group(0)!,
    );

    return translated == original ? original : translated;
  }

  static String _normalize(String value) {
    return value
        .trim()
        .toLowerCase()
        .replaceAll(RegExp(r'[_\-]+'), ' ')
        .replaceAll(RegExp(r'\s+'), ' ');
  }

